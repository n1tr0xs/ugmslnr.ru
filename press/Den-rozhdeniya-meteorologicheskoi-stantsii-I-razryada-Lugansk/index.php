          <p>Основанная 17 июня 1837 году при Луганском литейном заводе как метеорологическая обсерватория, она является одной из старейших метеостанций России. На метеорологической станции Луганск наблюдения проводятся на протяжении 187 лет. В 1925 году Луганская метеостанция вошла в состав Главной геофизической обсерватории, после чего получила не только всесоюзное, но и всемирное значение.</p>
