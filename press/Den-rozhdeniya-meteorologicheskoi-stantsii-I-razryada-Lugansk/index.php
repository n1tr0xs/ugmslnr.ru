<p> В рамках модернизации наблюдательной сети на метеорологической площадке было установлено новое оборудование. </p>
<p> Современная метеорологическая станция I разряда Луганск ФГБУ «УГМС по ЛНР» – это комплексная станция с метеорологическими, агрометеорологическими наблюдениями, наряду с которыми проводятся актинометрические наблюдения (определение суточных сумм суммарной солнечной радиации путем интегрирования) и наблюдения за радиационным фоном. </p>
