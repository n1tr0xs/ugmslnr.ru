<p>Перерывы в работе станции были только в отдельные месяцы 20-го столетия – во время гражданской (1920 г.) и Великой Отечественной войны (1941-1943гг.). В период украинской военной агрессии на Донбасс в результате обстрелов метеостанция временно (с 07 июля по 22 декабря 2014 года) прекратила работу. 23 декабря 2014 года наблюдения на метеостанции возобновились.</p>
<p>31.03.2023 метеорологическая станция I разряда Луганск вошла в состав федерального государственного бюджетного учреждения «Управление по гидрометеорологии и мониторингу окружающей среды по Луганской Народной Республике» (далее – ФГБУ «УГМС по ЛНР»).</p>
