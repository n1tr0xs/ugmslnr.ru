            дозы гамма-излучения на местности на данном пункте наблюдения от среднего значения, определяемое
            в соответствии с «Методическими указаниями по использованию дозиметров ДРГ-01Т на
            радиометрической сети станций», Обнинск, НПО «Тайфун», 1989 год. </li>
