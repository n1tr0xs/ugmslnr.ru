Мониторинг загрязнения атмосферного воздуха осуществляется в двух городах на восьми пунктах наблюдения за загрязнением атмосферного воздуха (ПНЗ): г. Луганск – 4 ПНЗ; г. Алчевск – 4 ПНЗ. Согласно программе работ, производятся наблюдения за концентрациями 9 показателей в Луганске и 9 показателей в Алчевске.
