        <p> Систематические наблюдения за состоянием окружающей среды, ее загрязнением, на территории деятельности ФГБУ «УГМС по ЛНР» организует и осуществляет Центр по мониторингу загрязнения окружающей среды (ЦМС). </p>
<p> В рамках Государственного задания Росгидромета, ЦМС проводит мониторинг загрязнения атмосферного воздуха, поверхностных вод суши, контроль за радиоактивным загрязнением окружающей среды, а также фоновым состоянием окружающей среды. Осуществляет свою деятельность под научно-техническим и организационно-методическим руководством научно-исследовательских учреждений (НИУ) Росгидромета. </p>
